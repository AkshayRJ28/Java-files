package Practice;

public class Cwh_23_for_loop {

	public static void main(String[] args) {

		for(int a=0;a<=10;a++)
		{
			System.out.println(a);
		}
		System.out.println("End for loop successfully");
		//wap print first n odd numbers
		
		for (int b =0; b<=10;b++)
		{
			{
				
				//2n = Even number = 2,4,6
				//2n+1= odd number =1,3,5
			
			 System.out.println(2*b+1);
				
			}
		}
	}

}
