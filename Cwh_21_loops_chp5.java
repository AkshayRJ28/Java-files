package Practice;

public class Cwh_21_loops_chp5 {

	public static void main(String[] args) {
		int i=1;
		while(i<5)
		{
			System.out.println(i);
			i++;
		}
		System.out.println("while loop is end");
		
		// do while loop 
		int b=0;
		do{
			System.out.println(b);
			b++;
		}
		while(b<5);
		
	}

}
